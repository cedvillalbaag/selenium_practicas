#2.- Crear carpeta y guardar el archivo.py

#3.- Abrir consola desde la carpeta que hemos creado

#4.- En consola escribir pyinstaller nombrearchivo.py + Enter (Nombrearchivo sin espacios)

#5.- En la carpeta del archivo ir a carpeta dist / carpetaproyecto / .exe

#6.- Verificamos el funcionamiento de programa, dando clic al archivo .exe   



